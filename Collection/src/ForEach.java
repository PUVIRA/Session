import java.util.*;

public class ForEach {

	public static void main(String[] args) {
		ArrayList<String>al=new ArrayList<String>();
		al.add("rahul");
		al.add("raj");
		al.add("ram");
		al.add("rohit");
		al.add("ritesh bail");
		for(String obj:al)
			System.out.println(obj);
	}

}
